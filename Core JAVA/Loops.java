public class Loops {
    public static void main(String[] args) {

        // While Loop
        int i = 0;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        // do while loop
        int j = 5;
        do {
            System.out.println(j);
            j--;
        } while (j >= 0);

        // for loop
        for (int k = 0; k <= 5; k++) {
            System.out.println(k);
        }

    }
}
