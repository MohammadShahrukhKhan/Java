// Strings , Arrrays etc. are the non-primitive data types.
// we can call the methods of these data types.

public class NonPrimitiveDataType {
    public static void main(String[] args) {
        // For Example
        String s = "Shahrukh";
        // charAt is is used to know the nth chracter in a string
        char ch = s.charAt(6); // such methods can be called in non-primitive data type or reference variables
        System.out.println(ch);
    }
}
