// byte, short, int, long, float, double, boolean, char are the primitive data types.

public class PrimitiveDataType {
    public static void main(String[] args) {
        byte a = 127;
        byte b = -128;
        System.out.println(a + b);
        double hh = 4.56321789d; // d is optional
        System.out.println(hh);
        char ii = 'J';
        System.out.println(ii);
    }
}
