public class FirstProgram {
    public static void main(String[] args) {
        // This is a comment
        System.out.println("Hello World");
        /*
         * This is a
         * multi-line comment
         */

        String Shahrukh = "A Good Boy";
        System.out.println(Shahrukh);

        int a = 45, e = 56, f = 64; // multiple Variables can be declared side by side
        float b = 4.5f; // f is compulsory
        char ch = 'a'; // only single character with single quotes
        System.out.println(a + b);
        System.out.println(ch);

        boolean c = true;
        System.out.print(c);
        System.out.println(e);
        System.out.println(f);

    }
}